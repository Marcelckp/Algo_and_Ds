console.log("Bitwise operations");

// Bitwise AND
console.log(5 & 13); // Output: 5

// Bitwise OR
console.log(5 | 13); // Output: 13

// Bitwise XOR
console.log(5 ^ 13); // Output: 8

// Bitwise NOT
console.log(~5); // Output: -6

// Left shift
console.log(5 << 1); // Output: 10

// Right shift
console.log(5 >> 1); // Output: 2

// Zero fill right shift
console.log(5 >>> 1); // Output: 2

